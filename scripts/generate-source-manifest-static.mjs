#!/usr/bin/env node

import { spawnSync } from "node:child_process";
import { existsSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const repoRoot = process.cwd();
const backendRepo = resolve(repoRoot, process.env.CREATOR_OPTIMIZER_REPO ?? "../creator_optimizer");
const outputPath = resolve(repoRoot, "src/lib/upload/source-manifest-static.generated.ts");
const explicitPython = process.env.CREATOR_OPTIMIZER_PYTHON?.trim() || null;
const bundledPython = resolve(
  backendRepo,
  process.platform === "win32" ? ".venv/Scripts/python.exe" : ".venv/bin/python",
);

function resolvePythonCommand() {
  if (explicitPython) {
    return explicitPython;
  }

  if (existsSync(bundledPython)) {
    return bundledPython;
  }

  return process.platform === "win32" ? "python" : "python3";
}

function buildPythonSnippet(repoPath) {
  return [
    "import json",
    "import sys",
    "from pathlib import Path",
    `repo = Path(r"${repoPath.replace(/\\/g, "\\\\")}")`,
    "sys.path.insert(0, str(repo / 'src'))",
    "from creator_optimizer.workspace_data_sources import build_source_manifest_payload",
    "print(json.dumps(build_source_manifest_payload(), indent=2))",
  ].join("\n");
}

const pythonCommand = resolvePythonCommand();
const result = spawnSync(pythonCommand, ["-c", buildPythonSnippet(backendRepo)], {
  cwd: repoRoot,
  encoding: "utf8",
});

if (result.error) {
  console.error(`[source-manifest:generate] failed to start ${pythonCommand}: ${result.error.message}`);
  process.exit(1);
}

if (result.status !== 0) {
  console.error(result.stderr || `[source-manifest:generate] failed to read canonical manifest from ${backendRepo}`);
  process.exit(result.status ?? 1);
}

const manifest = JSON.parse(result.stdout);
const output = `import type { SourceManifestResponse } from "../api/upload";

// Generated by \`scripts/generate-source-manifest-static.mjs\` from
// \`creator_optimizer.workspace_data_sources.build_source_manifest_payload()\`.
// Do not hand-edit this file.
export const STATIC_SOURCE_MANIFEST_RESPONSE: SourceManifestResponse = ${JSON.stringify(manifest, null, 2)};
`;

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output, "utf8");
console.log(`[source-manifest:generate] wrote ${outputPath}`);
